namespace FrEee.WinForms.DataGridView
{
	/// <summary>
	/// Display modes for progress bars.
	/// </summary>
	public enum ProgressDisplayMode
	{
		/// <summary>
		/// Displays the raw numbers.
		/// </summary>
		Raw,

		/// <summary>
		/// Displays numbers as percentages.
		/// </summary>
		Percentage,

		/// <summary>
		/// Displays estimated time to completion.
		/// </summary>
		Eta
	}
}